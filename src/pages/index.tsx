import type { NextPage } from "next";
import { useSession } from "next-auth/react";
import Head from "next/head";
import { trpc } from "../utils/trpc";

// react bootstrap
import Container from "react-bootstrap/Container";
import Row from "react-bootstrap/Row";
import Col from "react-bootstrap/Col";
import Spinner from "react-bootstrap/Spinner";
import Modal from "react-bootstrap/Modal";
import Image from "react-bootstrap/Image";

import Header from "../compontents/Header";
import FinalsCol from "../compontents/FinalsCol";
import AvailableCol from "../compontents/AvailableCol";
import WishList from "../compontents/WishList";

import { useState } from "react";
import { Alert } from "react-bootstrap";

const Home: NextPage = () => {
  //state
  const [showWinner, setShowWinner] = useState(false);

  // queries
  const { data: session } = useSession();
  // const {data: userData} = trpc.useQuery([""])
  let { data: unavailable, isLoading: gettingUnavailable } = trpc.useQuery([
    "movie.getUnavailable",
  ]);
  let { data: available, isLoading: gettingAvailble } = trpc.useQuery([
    "movie.getAvailable",
  ]);
  let { data: picked, isLoading: gettingPicked } = trpc.useQuery([
    "movie.getPicked",
  ]);
  let { data: winner } = trpc.useQuery(["movie.getWinner"]);

  unavailable = available || [];
  available = available || [];
  picked = picked || [];

  return (
    <div className="bg-blue-1">
      <Head>
        <title>Shit Screen</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <header className="container flex justify-between">
        <Header session={session} />
      </header>
      {gettingAvailble || gettingUnavailable || gettingPicked ? (
        <h2 className="text-center text-4xl">
          Loading <Spinner animation="border" />{" "}
        </h2>
      ) : (
        <main className="container">
          {winner && (
            <Modal show={showWinner} onHide={() => setShowWinner(false)}>
              <Modal.Header closeButton>
                <Modal.Title>The Winner is... {winner.Title}</Modal.Title>
              </Modal.Header>
              <Modal.Body>
                <Image src={winner.Poster} />
                <p>Metascore: {winner.Metascore}</p>
                <p>Rated {winner.Rated}</p>
                <p>Directed by {winner.Director}</p>
                <p>{winner.Runtime}</p>
                <p>{winner.Plot}</p>
              </Modal.Body>
            </Modal>
          )}{" "}
          <Container className="bg-blue-1">
            {winner && (
              <Alert
                variant="success"
                style={{ cursor: "pointer" }}
                onClick={() => setShowWinner(true)}
              >
                The winner is... {winner.Title}
              </Alert>
            )}
            <Row>
              <Col className="" sm={true} lg={true}>
                {picked?.length > 0 ? (
                  <FinalsCol
                    picked={picked}
                    session={session}
                    winner={winner}
                    showWinner={setShowWinner}
                  />
                ) : (
                  ""
                )}{" "}
              </Col>

              <Col className="" sm={true} lg={true}>
                <AvailableCol available={available} session={session} />
              </Col>

              <Col className="" sm={true} lg={true}>
                <WishList unavailable={unavailable} session={session} />
              </Col>
            </Row>
          </Container>
        </main>
      )}{" "}
    </div>
  );
};

export default Home;
