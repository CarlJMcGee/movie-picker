import type { NextPage } from "next";
import { useSession } from "next-auth/react";
import Head from "next/head";
import { trpc } from "../utils/trpc";

// react bootstrap
import Container from "react-bootstrap/Container";
import Row from "react-bootstrap/Row";
import Col from "react-bootstrap/Col";
import Spinner from "react-bootstrap/Spinner";
import Modal from "react-bootstrap/Modal";
import Image from "react-bootstrap/Image";

import Header from "../compontents/Header";
import FinalsCol from "../compontents/FinalsCol";
import AvailableCol from "../compontents/AvailableCol";
import WishList from "../compontents/WishList";

import { useEffect, useState } from "react";
import { Alert } from "react-bootstrap";
import pusherJs from "pusher-js";
import { MovieQuery } from "../types/imbd-data";
import { AnimatePresence } from "framer-motion";

const Home: NextPage = () => {
  //state
  const [winner, setWinner] = useState<MovieQuery | null>(null);
  const [showWinner, setShowWinner] = useState(false);

  const utils = trpc.useContext();

  // queries
  const { data: session } = useSession();
  let { data: unavailable, isLoading: gettingUnavailable } = trpc.useQuery([
    "movie.getUnavailable",
  ]);
  let { data: available, isLoading: gettingAvailble } = trpc.useQuery([
    "movie.getAvailable",
  ]);
  let { data: picked, isLoading: gettingPicked } = trpc.useQuery([
    "movie.getPicked",
  ]);

  unavailable = unavailable || [];
  available = available || [];
  picked = picked || [];

  // pusher websocket
  useEffect(() => {
    pusherJs.logToConsole = true;
    const pusher = new pusherJs("a180f97e989a0566ac2f", {
      cluster: "us2",
      forceTLS: true,
    });
    const mainChan = pusher.subscribe("main-channel");

    mainChan.bind("added_to_wishlist", () => {
      utils.invalidateQueries(["movie.getUnavailable"]);
    });

    mainChan.bind("removed_from_wishlist", () => {
      utils.invalidateQueries(["movie.getUnavailable"]);
    });

    mainChan.bind("made_available", () => {
      utils.invalidateQueries(["movie.getUnavailable"]);
      utils.invalidateQueries(["movie.getAvailable"]);
    });

    mainChan.bind("made_unavailable", () => {
      utils.invalidateQueries(["movie.getUnavailable"]);
      utils.invalidateQueries(["movie.getAvailable"]);
    });

    mainChan.bind("added_vote", () => {
      utils.invalidateQueries(["movie.getPicked"]);
    });
    mainChan.bind("removed_vote", () => {
      utils.invalidateQueries(["movie.getPicked"]);
    });

    mainChan.bind("we_have_a_winner", (movie: MovieQuery) => {
      setWinner(movie);
      setShowWinner(true);
    });

    mainChan.bind("reset", () => {
      utils.invalidateQueries(["movie.getWinner"]);
      utils.invalidateQueries(["movie.getPicked"]);
      setWinner(null);
    });
  }, []);

  return (
    <div className="bg-blue-1">
      <Head>
        <title>Shit Screen</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <header className="container flex justify-between">
        <Header session={session} />
      </header>
      {gettingAvailble || gettingUnavailable || gettingPicked ? (
        <h2 className="text-center text-4xl">
          Loading <Spinner animation="border" />{" "}
        </h2>
      ) : (
        <main className="container">
          {winner && (
            <Modal show={showWinner} onHide={() => setShowWinner(false)}>
              <Modal.Header closeButton>
                <Modal.Title>The Winner is... {winner.Title}</Modal.Title>
              </Modal.Header>
              <Modal.Body>
                <Image src={winner.Poster} alt="winning movie's poster" />
                <p>Metascore: {winner.Metascore}</p>
                <p>Rated {winner.Rated}</p>
                <p>Directed by {winner.Director}</p>
                <p>{winner.Runtime}</p>
                <p>{winner.Plot}</p>
              </Modal.Body>
            </Modal>
          )}{" "}
          <Container className="bg-blue-1">
            {winner && (
              <Alert
                variant="success"
                style={{ cursor: "pointer" }}
                onClick={() => setShowWinner(true)}
              >
                The winner is... {winner.Title}
              </Alert>
            )}
            <Row>
              <Col className="" sm={true} lg={true}>
                {picked?.length > 0 ? (
                  <FinalsCol
                    picked={picked}
                    session={session}
                    winner={winner}
                    showWinner={setShowWinner}
                  />
                ) : (
                  ""
                )}{" "}
              </Col>

              <Col className="" sm={true} lg={true}>
                <AvailableCol available={available} session={session} />
              </Col>

              <Col className="" sm={true} lg={true}>
                <WishList unavailable={unavailable} session={session} />
              </Col>
            </Row>
          </Container>
        </main>
      )}{" "}
    </div>
  );
};

export default Home;
